const frutas = ['Maça', 'Pera', 'Uva', 'Banana', 'Manga', 'Kiwi'];

// Percorre o array `frutas`
for( let i = 0; i < frutas.length; i++ ) {
    console.log(`Indíce ${i}`, frutas[i]);
}

//Percorre pulando de 10 em 10
for( let a = 0; a <= 100; a += 10 ) {
    console.log( a );
}

// Decrementar 
for( let b = 12; b >= 0; b-- ) {
    console.log(b)
}