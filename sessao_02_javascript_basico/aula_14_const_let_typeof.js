/**
 * Variavel
 * Para criar variavel utilizar a palavra reservada let, 
 * ao invés do var que está em desuso
 */
let nomeCompleto = 'Fernando'
let idade = 32

console.log('Nome:', nomeCompleto)
console.log('Idade:', idade)

/**
 * Constant
 */
const primeiroNumero = 10
const segundoNumero = 20
const somaNumero = primeiroNumero + segundoNumero

console.log(primeiroNumero)
console.log(segundoNumero)
console.log(somaNumero)


